  //           widget.isGroupChat,
  //         );
  //   }
  // }
  void sendTextMessage() async {
    if (isShowSendButton) {
      ref.read(chatControllerProvider).sendTextMessage(
          context, _messageController.text.trim(), widget.recieverUserId
          //widget.isGroupChat,
          );
      setState(() {
        _messageController.text = '';
      });
    }
  }

  // void sendFileMessage(
